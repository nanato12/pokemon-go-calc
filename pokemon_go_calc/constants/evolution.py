def get_chain_base(dex: int) -> int:
    """図鑑番号から進化チェーンの基本形を取得.

    Args:
        dex: 図鑑番号

    Returns:
        進化チェーンの基本形の図鑑番号
    """
    for base, chain in EVOLUTION_CHAINS.items():
        if dex in chain:
            return base
    return dex


def get_evolution_chain(dex: int) -> list[int]:
    """図鑑番号から進化チェーンを取得.

    Args:
        dex: 図鑑番号

    Returns:
        進化チェーンの図鑑番号リスト
    """
    base = get_chain_base(dex)
    return EVOLUTION_CHAINS.get(base, [dex])
